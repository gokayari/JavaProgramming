package day03_EscapeSequences;

public class Err_Vs_Out {

    public static void main(String[] args) {

        System.out.println("Hello World!"); // regular outputs (white or black)

        System.out.println("-------------------");

        System.err.println("Hello World!"); // used for printing error messages (red)

        System.err.println("-------------------");
    }
}
