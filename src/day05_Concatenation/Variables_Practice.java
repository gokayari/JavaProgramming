package day05_Concatenation;

public class Variables_Practice {

    public static void main(String[] args) {

        byte num1 = 100;
        short num2 = 10000;
        double num3 = 3.5f;
        float num4 = 2.5F; // write "F or f" for float
        long num5 = 999999999999L;
        int num6 = 850000; // or you can use also "long", "double", "float"

        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);
        System.out.println("num3 = " + num3);
        System.out.println("num4 = " + num4);
        System.out.println("num5 = " + num5);
        System.out.println("num6 = " + num6);


        //***Extra:

        int a = 'A'; //  According to the ASCII table, the Integer value of "A" is 65.
        System.out.println("a = " + a);
        char b = 'B';
        System.out.println("b = " + b);


    }
}
